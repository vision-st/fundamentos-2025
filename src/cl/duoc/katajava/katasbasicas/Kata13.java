package cl.duoc.katajava.katasbasicas;

public class Kata13 {
    public static void ejecutar() {
        int i = 1, suma = 0;
        while (i <= 5) {
            suma += i;
            i++;
        }
        System.out.println("Kata13 - La suma de 1 a 5 es: " + suma);
    }
}