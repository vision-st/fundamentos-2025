
package cl.duoc.katajava.katasbasicas;

/**
 * Kata8 - Verificar nota aprobatoria
 * <p>
 * Algoritmo: evalúa si la nota es mayor o igual a 60.
 * Teoría aplicada: if/else, operadores de comparación, booleanos.
 */
public class Kata8 {
    public static void ejecutar() {
        int nota = 75;
        if (nota >= 60)
            System.out.println("Kata8 - Aprobado ");
        else
            System.out.println("Kata8 - Reprobado ");
    }
}

